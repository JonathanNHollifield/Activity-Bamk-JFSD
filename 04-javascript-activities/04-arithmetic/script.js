// One at a time, uncomment the line that starts with: let 
// Don't forget to name your variable
let num1 = 3
let num2 = 2

// THIS SHOULD ADD
let addition = num1 + num2;
console.log(addition);

// THIS SHOULD SUBTRACT NUM1 FROM NUM2 
let subtraction = num1 - num2;
console.log(subtraction);

// THIS SHOULD MULTIPLY NUM1 & NUM2 
let multiplication = num1 * num2;
console.log(multiplication);

// THIS SHOULD DIVIDE NUM1 FROM NUM2 
let divide = num1 / num2;
console.log(divide);
// THIS SHOULD FIND THE MODULUS OF NUM1 FROM NUM2 

let modulus = num1 % num2;
console.log(modulus);

